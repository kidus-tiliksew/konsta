<script>
  import { TableRowClasses } from '../../shared/esm/classes/TableRowClasses.js';
  import { TableRowColors } from '../../shared/esm/colors/TableRowColors.js';
  import { useDarkClasses } from '../shared/use-dark-classes.js';
  import { useThemeClasses } from '../shared/use-theme-classes.js';

  let className = undefined;
  export { className as class };
  let colorsProp = undefined;
  export { colorsProp as colors };

  export let ios = undefined;
  export let material = undefined;

  export let header = undefined;

  const rippleEl = { current: null };
  const dark = useDarkClasses();
  $: colors = TableRowColors(colorsProp, dark);

  $: c = useThemeClasses(
    { ios, material },
    TableRowClasses({ header }, colors),
    className,
    (v) => (c = v)
  );
</script>

<tr bind:this={rippleEl.current} class={c.base} {...$$restProps}>
  <slot />
</tr>
